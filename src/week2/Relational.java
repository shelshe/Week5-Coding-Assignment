package week2;

public class Relational {

	public static void main(String[] args) {

		int number1 = 10;
		int number2 = 15;
		
		System.out.println(number1 == number2);
		System.out.println(number1 != number2);
		System.out.println(number1 > number2);
		System.out.println(number1 < number2);
		System.out.println(number1 <= number2);
		System.out.println(number1 >= number2);
	}

}
